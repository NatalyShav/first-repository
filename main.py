# Запрашиваем имя пользователя
name = input("Введите ваше имя: ")

# Запрашиваем возраст пользователя
age = int(input("Введите ваш возраст: "))

# Выводим приветствие
print(f"Привет, {name}! Тебе {age} лет.")

# Переводим возраст в дни, часы и секунды
days = age * 365  # Примерно, без учета високосных годов
hours = days * 24
seconds = hours * 3600

# Выводим результаты
print(f"Это примерно {days} дней, {hours} часов или {seconds} секунд.")